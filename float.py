# float variables
num1 = 10.5
num2 = 9.8

# addition
result1 = num1 + num2
print("Addition", result1)

# Subtraction
result2 = num1 - num2
print("Substraction", result2)

# Multiplication
result3 = num1 * num2
print("Multiplication", result3)

# Division
result4 = num1 / num2
print("Division", result4)

# round
result5 = round(result2, 2)
print("Round", result5)
